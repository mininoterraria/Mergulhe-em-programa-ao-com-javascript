//Crie uma variável "numero" e peça um valor com prompt verifique se é positivo, negativo ou zero. Use if-else para imprimir a respectiva mensagem
let numero = prompt("Insira um número");
if(numero > 0){
    alert("É positivo.");
}else if(numero == 0){
    alert("É zero.");
}else{
    alert("É negativo");
}